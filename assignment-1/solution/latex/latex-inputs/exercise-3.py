import sys
import os
from subprocess import call
from numpy import *
import matplotlib.pyplot as plt
#-------------------------------------------------------------------------#
# User packages                                                           #
#-------------------------------------------------------------------------#
from ales_post.plot_settings import plot_setting
#=========================================================================#
# Main preamble                                                           #
#=========================================================================#
if __name__ == '__main__':
    #---------------------------------------------------------------------#
    # Main preamble                                                       #
    #---------------------------------------------------------------------#
    call(['clear'])
    sep         = os.sep
    pwd         = os.getcwd()
    media_path  = pwd + '%c..%cmedia%c'         %(sep, sep, sep)
    #---------------------------------------------------------------------#
    # Domain variables                                                    #
    #---------------------------------------------------------------------#
    x   = linspace(-10, -5, 100)
    y1  = 4./3. - 2./3*x
    y2  = 5./2.-x/2.
    #---------------------------------------------------------------------#
    # Plotting solution                                                   #
    #---------------------------------------------------------------------#
    plot_setting()
    plt.plot([-10,-7], [6,6], 'k--', lw = 3.0)
    plt.plot([-7,-7], [4,6], 'k--', lw = 3.0)
    plt.plot(x,y1,'r', lw=1.5, label = '$y = 4/3 - 2/3 x$')
    plt.plot(x,y2,'b', lw=1.5, label = '$y = 5/2 - 1/2 x$')
    #---------------------------------------------------------------------#
    # Plot settings                                                       #
    #---------------------------------------------------------------------#
    plt.legend(loc=0)
    plt.ylabel('$y$')
    plt.xlabel('$x$')
    plt.grid()
    plt.xlim([-10,-5])
    plt.ylim([4,8])
    plt.savefig(media_path + 'exercise-3.png')
    plt.close()

    print('**** Successful run ****')
    sys.exit(0)
